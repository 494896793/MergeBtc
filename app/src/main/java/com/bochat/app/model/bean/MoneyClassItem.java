package com.bochat.app.model.bean;

import java.io.Serializable;

/**
 * 2019/5/4
 * Author LDL
 **/
public class MoneyClassItem extends CodeEntity implements Serializable {


    private String tname;
    private int type;
    private int id;

    @Override
    public String toString() {
        return "MoneyClassItem{" +
                "tname='" + tname + '\'' +
                ", type=" + type +
                ", id=" + id +
                '}';
    }

    public String getTname() {
        return tname;
    }

    public void setTname(String tname) {
        this.tname = tname;
    }

    public int getType() {
        return type;
    }

    public void setType(int type) {
        this.type = type;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
}

public class TokenExchangeResultEntity extends CodeEntity implements Serializable {
    private String currencyNameSource;
    private double currencyAmountSource;
    private String currencyNameTarget;
    private double currencyAmountTarget;
    private double exchangeRate;

    public String getCurrencyNameSource() {
        return currencyNameSource;
    }

    public void setCurrencyNameSource(String currencyNameSource) {
        this.currencyNameSource = currencyNameSource;
    }

    public double getCurrencyAmountSource() {
        return currencyAmountSource;
    }

    public void setCurrencyAmountSource(double currencyAmountSource) {
        this.currencyAmountSource = currencyAmountSource;
    }

    public String getCurrencyNameTarget() {
        return currencyNameTarget;
    }

    public void setCurrencyNameTarget(String currencyNameTarget) {
        this.currencyNameTarget = currencyNameTarget;
    }

    public double getCurrencyAmountTarget() {
        return currencyAmountTarget;
    }

    public void setCurrencyAmountTarget(double currencyAmountTarget) {
        this.currencyAmountTarget = currencyAmountTarget;
    }

    public double getExchangeRate() {
        return exchangeRate;
    }

    public void setExchangeRate(double exchangeRate) {
        this.exchangeRate = exchangeRate;
    }


    @Override
    public String toString() {
        return "TokenExchangeResultEntity{" +
                "currencyNameSource='" + currencyNameSource + '\'' +
                ", currencyAmountSource=" + currencyAmountSource +
                ", currencyNameTarget='" + currencyNameTarget + '\'' +
                ", currencyAmountTarget=" + currencyAmountTarget +
                ", exchangeRate=" + exchangeRate +
                '}';
    }
}

package com.bochat.app.model.bean;

import java.io.Serializable;
import java.util.List;

/**
 * 2019/6/14
 * Author ZZW
 **/
public class ShakyListEntity extends CodeEntity implements Serializable {

    private List<ShakyEntity> items;
    private int currentPage;
    private int isNext;
    private int pageSize;
    private int totalCount;
    private int totalPage;

    public List<ShakyEntity> getItems() {
        return items;
    }

    public void setItems(List<ShakyEntity> items) {
        this.items = items;
    }

    public int getCurrentPage() {
        return currentPage;
    }

    public void setCurrentPage(int currentPage) {
        this.currentPage = currentPage;
    }

    public int getIsNext() {
        return isNext;
    }

    public void setIsNext(int isNext) {
        this.isNext = isNext;
    }

    public int getPageSize() {
        return pageSize;
    }

    public void setPageSize(int pageSize) {
        this.pageSize = pageSize;
    }

    public int getTotalCount() {
        return totalCount;
    }

    public void setTotalCount(int totalCount) {
        this.totalCount = totalCount;
    }

    public int getTotalPage() {
        return totalPage;
    }

    public void setTotalPage(int totalPage) {
        this.totalPage = totalPage;
    }

    @Override
    public String toString() {
        return "ShakyListEntity{" +
                "items=" + items +
                ", currentPage=" + currentPage +
                ", isNext=" + isNext +
                ", pageSize=" + pageSize +
                ", totalCount=" + totalCount +
                ", totalPage=" + totalPage +
                '}';
    }
}

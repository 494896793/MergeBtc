public class RouterSetLoginPassword extends AbstractRouter{
    public static final String PATH ="/path/RouterSetLoginPassword";
    
    private String phoneNum;
    private String verifyCode;

    public RouterSetLoginPassword() {
    }

    public RouterSetLoginPassword(String phoneNum, String verifyCode) {
        this.phoneNum = phoneNum;
        this.verifyCode = verifyCode;
    }

    public String getPhoneNum() {
        return phoneNum;
    }

    public void setPhoneNum(String phoneNum) {
        this.phoneNum = phoneNum;
    }

    public String getVerifyCode() {
        return verifyCode;
    }

    public void setVerifyCode(String verifyCode) {
        this.verifyCode = verifyCode;
    }

    @Override
    public String getPath() {
        return PATH;
    }
}
